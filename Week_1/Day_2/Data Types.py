"""
Data Types
Attributes that define what kind of data a value can hold and what operations can be performed on it, serving as
category for a single piece of data

Strings - Represents sequences of characters (strings), enclosed in single or double quotes.

Integers - Represents whole numbers (integers), positive, negative, or zero

Floats - Represents real numbers with decimal points (floating-point numbers).

Booleans - Represents truth values, either True or False.

"""
# Subscripting
print("Hello"[-1])

# String
print("123" + "456")

# Integer = Whole number
print(123 + 456)

# Large Integers
print(123_456_789)

# Float
print(3.14159)

# Boolean
print(True)
print(False)
